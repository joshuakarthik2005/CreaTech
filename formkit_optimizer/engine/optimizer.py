"""
FormKit Optimizer — Multi-Period Formwork Kit Optimization Engine
================================================================

This module implements the core MIP (Mixed-Integer Programming) formulation
for optimizing formwork kitting across a multi-period pour schedule.

Solver: Google OR-Tools CP-SAT (primary) / HiGHS via highspy (fallback)

The formulation minimizes total formwork cost across the project by jointly
optimizing:
  - Kit configuration selection per pour
  - Component-to-pour assignment with reuse tracking
  - Procurement vs. rental decisions
  - Multi-period inventory balance

Domain: Construction formwork planning for multi-storey residential towers.
"""

from __future__ import annotations

import logging
import uuid
from dataclasses import dataclass, field
from datetime import date, timedelta
from enum import Enum
from typing import Optional

from ortools.sat.python import cp_model

logger = logging.getLogger(__name__)


# ============================================================
# DATA CLASSES — Typed inputs to the optimizer
# ============================================================

class ComponentType(Enum):
    PANEL = "PANEL"
    FILLER = "FILLER"
    TIE_ROD = "TIE_ROD"
    TIE_CONE = "TIE_CONE"
    WALER = "WALER"
    PROP = "PROP"
    CLAMP = "CLAMP"
    WEDGE = "WEDGE"
    CORNER = "CORNER"
    ACCESSORY = "ACCESSORY"


class PourType(Enum):
    WALL = "WALL"
    SLAB = "SLAB"
    COLUMN = "COLUMN"
    BEAM = "BEAM"
    SHEAR_WALL = "SHEAR_WALL"


@dataclass
class Component:
    """A formwork component type (not a physical instance)."""
    component_id: str
    system_code: str
    component_type: ComponentType
    description: str
    width_mm: Optional[int] = None
    height_mm: Optional[int] = None
    weight_kg: float = 0.0
    unit_cost_buy: float = 0.0
    unit_cost_rent_per_day: float = 0.0
    loss_rate_per_cycle: float = 0.002
    damage_rate_per_cycle: float = 0.005
    rated_reuse_cycles: int = 300


@dataclass
class CompatibilityRule:
    """Defines quantity ratio between components (e.g., ties per panel)."""
    source_component_id: str
    target_component_id: str
    quantity_ratio: float  # target_qty = source_qty * ratio
    notes: str = ""


@dataclass
class KitConfig:
    """A candidate kit configuration for a specific pour.
    
    Generated by the Panel Layout Engine before optimization.
    Each pour typically has 2-5 candidate configs.
    """
    config_id: str
    pour_id: str
    component_demands: dict[str, int]  # component_id -> quantity needed
    total_panel_area_m2: float
    coverage_ratio: float
    estimated_handling_cost: float  # assembly + strip labor
    description: str = ""


@dataclass
class Pour:
    """A concrete pour event with schedule and formwork demand."""
    pour_id: str
    tower_code: str
    floor_number: int
    zone_code: str
    pour_type: PourType
    planned_date: date
    net_surface_area_m2: float
    concrete_grade: str
    strip_cycle_hours: int
    assigned_system_code: str
    candidate_configs: list[KitConfig] = field(default_factory=list)
    is_locked: bool = False          # planner has locked this pour's kit
    locked_config_id: str | None = None

    @property
    def strip_date(self) -> date:
        """Earliest date formwork can be stripped."""
        strip_days = max(1, self.strip_cycle_hours // 24)
        return self.planned_date + timedelta(days=strip_days)

    @property
    def available_date(self) -> date:
        """Date stripped components are cleaned and available for reuse.
        Assumes 0.5-day cleaning buffer after stripping."""
        return self.strip_date + timedelta(days=1)

    @property
    def erection_start_date(self) -> date:
        """Formwork erection starts 1 day before pour."""
        return self.planned_date - timedelta(days=1)


@dataclass
class InventoryPosition:
    """Current inventory of a component type at optimization start."""
    component_id: str
    qty_available: int
    qty_deployed: int  # will become available per deployed_pour schedule
    avg_remaining_cycles: float


@dataclass
class DeployedAllocation:
    """Components currently deployed on an active pour (not yet stripped)."""
    component_id: str
    pour_id: str
    quantity: int
    expected_available_date: date


# ============================================================
# OPTIMIZER RESULT TYPES
# ============================================================

@dataclass
class KitAssignment:
    """Result: which kit config was selected and how components are sourced."""
    pour_id: str
    selected_config_id: str
    component_assignments: list[ComponentAssignment]
    total_cost: float
    explanation: str


@dataclass
class ComponentAssignment:
    """Result: sourcing detail for one component in a kit."""
    component_id: str
    quantity: int
    source: str  # "INVENTORY", "REUSE_FROM:<pour_id>", "PROCURE", "RENT"
    available_from: date
    cost: float
    reason: str  # human-readable explanation


@dataclass
class ProcurementPlan:
    """Result: what to buy or rent, and when."""
    component_id: str
    quantity: int
    action: str  # "BUY" or "RENT"
    needed_by: date
    cost: float
    justification: str


@dataclass
class OptimizationResult:
    """Complete output of an optimization run."""
    run_id: str
    project_id: str
    status: str  # "OPTIMAL", "FEASIBLE", "INFEASIBLE", "TIMEOUT"
    objective_value: float
    solve_time_seconds: float
    optimality_gap: float
    kit_assignments: list[KitAssignment]
    procurement_plan: list[ProcurementPlan]
    inventory_trajectory: dict[str, list[tuple[date, int]]]  # component_id -> [(date, qty_available)]
    total_cost_breakdown: dict[str, float]


# ============================================================
# PANEL LAYOUT ENGINE
# ============================================================

class PanelLayoutEngine:
    """Generates candidate kit configurations for a pour.
    
    Given a pour's surface area and the assigned formwork system,
    produces 2-5 feasible panel arrangements with varying trade-offs
    between large panels (less handling) and small panels (more flexibility).
    """

    # Standard PERI TRIO panel widths in mm, ordered descending
    TRIO_WIDTHS = [2400, 1200, 900, 600, 450, 330, 240]
    TRIO_FILLERS = [150, 100, 50]

    def __init__(self, components: list[Component], rules: list[CompatibilityRule]):
        self.components = {c.component_id: c for c in components}
        self.components_by_system: dict[str, list[Component]] = {}
        for c in components:
            self.components_by_system.setdefault(c.system_code, []).append(c)
        self.rules = rules

    def generate_configs(self, pour: Pour, max_configs: int = 3) -> list[KitConfig]:
        """Generate candidate kit configurations for a pour."""
        if pour.pour_type == PourType.WALL:
            return self._generate_wall_configs(pour, max_configs)
        elif pour.pour_type == PourType.SLAB:
            return self._generate_slab_configs(pour, max_configs)
        else:
            # Column, beam — simplified single-config
            return self._generate_simple_config(pour)

    def _generate_wall_configs(self, pour: Pour, max_configs: int) -> list[KitConfig]:
        """Generate wall kit configurations.
        
        Assumes wall formwork is applied to BOTH faces.
        Wall length derived from surface area / (2 * wall height).
        """
        system_code = pour.assigned_system_code
        panels = [c for c in self.components_by_system.get(system_code, [])
                  if c.component_type == ComponentType.PANEL]
        if not panels:
            logger.warning(f"No panels found for system {system_code}")
            return []

        # Derive wall dimensions (both faces)
        wall_height_m = 2.7  # default TRIO height
        total_length_m = pour.net_surface_area_m2 / wall_height_m  # both faces combined
        single_face_length_mm = int((total_length_m / 2) * 1000)

        configs = []
        strategies = [
            ("MAX_LARGE", "Maximize large panels — fewer pieces, less handling"),
            ("BALANCED", "Balanced mix — moderate handling, good reuse flexibility"),
            ("MAX_FLEXIBLE", "Maximum flexibility — smaller panels, easier redistribution"),
        ]

        for idx, (strategy_name, desc) in enumerate(strategies[:max_configs]):
            demands = self._layout_panels_for_wall(
                single_face_length_mm, system_code, strategy_name, panels
            )
            # Double for both faces
            for comp_id in list(demands.keys()):
                comp = self.components.get(comp_id)
                if comp and comp.component_type == ComponentType.PANEL:
                    demands[comp_id] *= 2

            # Add accessories via compatibility rules
            demands = self._add_accessories(demands, system_code)

            total_panel_area = sum(
                (self.components[cid].width_mm or 0) * (self.components[cid].height_mm or 0) / 1e6 * qty
                for cid, qty in demands.items()
                if self.components.get(cid) and self.components[cid].component_type == ComponentType.PANEL
            )

            handling_cost = sum(
                qty * self.components[cid].weight_kg * 0.5  # ₹0.5 per kg handling estimate
                for cid, qty in demands.items()
                if cid in self.components
            )

            config = KitConfig(
                config_id=f"{pour.pour_id}-CFG-{chr(65 + idx)}",
                pour_id=pour.pour_id,
                component_demands=demands,
                total_panel_area_m2=total_panel_area,
                coverage_ratio=total_panel_area / pour.net_surface_area_m2 if pour.net_surface_area_m2 > 0 else 0,
                estimated_handling_cost=handling_cost,
                description=desc,
            )
            configs.append(config)

        return configs

    def _layout_panels_for_wall(
        self, length_mm: int, system_code: str, strategy: str,
        panels: list[Component]
    ) -> dict[str, int]:
        """Bin-packing panels to cover a wall face of given length.
        
        Strategy controls which panel sizes are preferred:
        - MAX_LARGE: Start with largest panels
        - BALANCED: Mix of large and medium
        - MAX_FLEXIBLE: Prefer smaller, more numerous panels
        """
        available_widths = sorted(
            [(p.width_mm, p.component_id) for p in panels if p.width_mm],
            key=lambda x: x[0], reverse=True
        )

        if strategy == "MAX_FLEXIBLE":
            # Skip the largest panel size
            if len(available_widths) > 1:
                available_widths = available_widths[1:]
        elif strategy == "BALANCED":
            # Start with second-largest
            if len(available_widths) > 2:
                available_widths = available_widths[1:]

        demands: dict[str, int] = {}
        remaining = length_mm

        for width, comp_id in available_widths:
            if remaining <= 0:
                break
            count = remaining // width
            if count > 0:
                demands[comp_id] = count
                remaining -= count * width

        # Fill remainder with fillers
        if remaining > 0:
            fillers = [c for c in self.components_by_system.get(system_code, [])
                       if c.component_type == ComponentType.FILLER]
            filler_widths = sorted(
                [(f.width_mm, f.component_id) for f in fillers if f.width_mm],
                key=lambda x: x[0], reverse=True
            )
            for fw, fid in filler_widths:
                if remaining <= 0:
                    break
                fcount = remaining // fw
                if fcount > 0:
                    demands[fid] = fcount
                    remaining -= fcount * fw

        return demands

    def _generate_slab_configs(self, pour: Pour, max_configs: int) -> list[KitConfig]:
        """Simplified slab config — area-based panel count + props."""
        system_code = pour.assigned_system_code or "PERI_GRIDFLEX"
        panels = [c for c in self.components_by_system.get(system_code, [])
                  if c.component_type == ComponentType.PANEL]
        props = [c for c in self.components_by_system.get(system_code, [])
                 if c.component_type == ComponentType.PROP]

        if not panels:
            return self._generate_simple_config(pour)

        # Pick the largest panel
        panel = max(panels, key=lambda p: (p.width_mm or 0) * (p.height_mm or 0))
        panel_area = ((panel.width_mm or 0) * (panel.height_mm or 0)) / 1e6
        if panel_area <= 0:
            return self._generate_simple_config(pour)

        num_panels = int(pour.net_surface_area_m2 / panel_area) + 1
        # Props: 1 per 2m² of slab area
        num_props = int(pour.net_surface_area_m2 / 2.0) + 1

        demands = {panel.component_id: num_panels}
        if props:
            demands[props[0].component_id] = num_props

        config = KitConfig(
            config_id=f"{pour.pour_id}-CFG-A",
            pour_id=pour.pour_id,
            component_demands=demands,
            total_panel_area_m2=num_panels * panel_area,
            coverage_ratio=(num_panels * panel_area) / pour.net_surface_area_m2,
            estimated_handling_cost=num_panels * (panel.weight_kg * 0.5),
            description="Standard slab panel coverage with props",
        )
        return [config]

    def _generate_simple_config(self, pour: Pour) -> list[KitConfig]:
        """Fallback: generate a placeholder config based on surface area."""
        return [KitConfig(
            config_id=f"{pour.pour_id}-CFG-A",
            pour_id=pour.pour_id,
            component_demands={},
            total_panel_area_m2=pour.net_surface_area_m2,
            coverage_ratio=1.0,
            estimated_handling_cost=pour.net_surface_area_m2 * 50,
            description="Placeholder — requires manual component specification",
        )]

    def _add_accessories(self, demands: dict[str, int], system_code: str) -> dict[str, int]:
        """Apply compatibility rules to add tie rods, cones, clamps, etc."""
        added: dict[str, int] = {}
        for rule in self.rules:
            if rule.source_component_id in demands and rule.quantity_ratio:
                source_qty = demands[rule.source_component_id]
                target_qty = max(1, int(source_qty * rule.quantity_ratio))
                added[rule.target_component_id] = (
                    added.get(rule.target_component_id, 0) + target_qty
                )
        # Merge
        result = dict(demands)
        for comp_id, qty in added.items():
            result[comp_id] = result.get(comp_id, 0) + qty
        return result


