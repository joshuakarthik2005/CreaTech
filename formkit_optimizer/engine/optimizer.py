# MULTI-PERIOD OPTIMIZER (CP-SAT FORMULATION)
# ============================================================

class FormworkOptimizer:
    """Multi-period formwork kit optimizer using CP-SAT.
    
    Solves the joint problem of:
    1. Kit configuration selection per pour
    2. Component assignment (inventory reuse vs. new procurement vs. rental)
    3. Multi-period inventory balance across all time periods
    
    Objective: Minimize total project formwork cost.
    """

    # Cost scaling factor (CP-SAT works with integers)
    COST_SCALE = 100  # Store costs in paise (1/100 of rupee)

    def __init__(
        self,
        pours: list[Pour],
        components: list[Component],
        initial_inventory: list[InventoryPosition],
        deployed_allocations: list[DeployedAllocation],
        handling_cost_per_kg: float = 0.5,
        holding_cost_per_unit_per_day: float = 2.0,
        max_solve_time_seconds: int = 300,
    ):
        self.pours = sorted(pours, key=lambda p: p.planned_date)
        self.components = {c.component_id: c for c in components}
        self.initial_inventory = {ip.component_id: ip for ip in initial_inventory}
        self.deployed = deployed_allocations
        self.handling_cost_per_kg = handling_cost_per_kg
        self.holding_cost_per_unit_per_day = holding_cost_per_unit_per_day
        self.max_solve_time = max_solve_time_seconds

        # Build time index
        self._build_time_index()

    def _build_time_index(self):
        """Map pours and dates to integer time periods."""
        all_dates = set()
        for p in self.pours:
            all_dates.add(p.erection_start_date)
            all_dates.add(p.planned_date)
            all_dates.add(p.strip_date)
            all_dates.add(p.available_date)
        for da in self.deployed:
            all_dates.add(da.expected_available_date)

        self.date_list = sorted(all_dates)
        self.date_to_period = {d: i for i, d in enumerate(self.date_list)}
        self.num_periods = len(self.date_list)

    def solve(self) -> OptimizationResult:
        """Build and solve the MIP model. Returns OptimizationResult."""
        run_id = str(uuid.uuid4())
        model = cp_model.CpModel()

        # Shorthand
        pours = self.pours
        comp_ids = list(self.components.keys())
        n_pours = len(pours)
        n_comps = len(comp_ids)

        logger.info(f"Building model: {n_pours} pours, {n_comps} component types, {self.num_periods} periods")

        # ============================================================
        # DECISION VARIABLES
        # ============================================================

        # z[p][k] = 1 if kit config k is selected for pour p
        z: dict[int, dict[str, object]] = {}
        for pi, pour in enumerate(pours):
            if pour.is_locked and pour.locked_config_id:
                # Locked — no choice variable needed
                z[pi] = {}
                continue
            z[pi] = {}
            for config in pour.candidate_configs:
                z[pi][config.config_id] = model.NewBoolVar(f"z_{pi}_{config.config_id}")

        # x[p][c] = quantity of component c assigned to pour p
        x: dict[int, dict[str, object]] = {}
        for pi, pour in enumerate(pours):
            x[pi] = {}
            for cid in comp_ids:
                max_demand = max(
                    (cfg.component_demands.get(cid, 0) for cfg in pour.candidate_configs),
                    default=0
                )
                if max_demand > 0:
                    x[pi][cid] = model.NewIntVar(0, max_demand * 2, f"x_{pi}_{cid}")

        # b[c][t] = new procurement of component c at time period t
        b: dict[str, dict[int, object]] = {}
        for cid in comp_ids:
            b[cid] = {}
            for t in range(self.num_periods):
                b[cid][t] = model.NewIntVar(0, 500, f"b_{cid}_{t}")

        # r[c][t] = rental quantity of component c active at time period t
        r: dict[str, dict[int, object]] = {}
        for cid in comp_ids:
            r[cid] = {}
            for t in range(self.num_periods):
                r[cid][t] = model.NewIntVar(0, 200, f"r_{cid}_{t}")

        # I[c][t] = idle inventory of component c at end of period t
        inv: dict[str, dict[int, object]] = {}
        for cid in comp_ids:
            inv[cid] = {}
            for t in range(self.num_periods):
                inv[cid][t] = model.NewIntVar(0, 1000, f"I_{cid}_{t}")

        # ============================================================
        # CONSTRAINTS
        # ============================================================

        # C1 + C2: Kit config selection — exactly one per pour
        for pi, pour in enumerate(pours):
            if pour.is_locked:
                continue
            if not pour.candidate_configs:
                continue
            model.AddExactlyOne(list(z[pi].values()))

        # Link x[p][c] to selected config's demand
        for pi, pour in enumerate(pours):
            if pour.is_locked and pour.locked_config_id:
                # Fixed assignment
                locked_cfg = next(
                    (cfg for cfg in pour.candidate_configs if cfg.config_id == pour.locked_config_id),
                    None
                )
                if locked_cfg:
                    for cid, qty in locked_cfg.component_demands.items():
                        if cid in x[pi]:
                            model.Add(x[pi][cid] == qty)
                continue

            for cid in comp_ids:
                if cid not in x[pi]:
                    continue
                # x[p][c] must equal the demand of the selected config
                for config in pour.candidate_configs:
                    demand = config.component_demands.get(cid, 0)
                    if config.config_id in z[pi]:
                        # If this config is selected, x must >= demand
                        model.Add(
                            x[pi][cid] >= demand
                        ).OnlyEnforceIf(z[pi][config.config_id])
                        model.Add(
                            x[pi][cid] <= demand
                        ).OnlyEnforceIf(z[pi][config.config_id])

        # C3: Inventory balance across periods
        for cid in comp_ids:
            for t in range(self.num_periods):
                current_date = self.date_list[t]

                # Components returning from deployed pours on this date
                returning = 0
                for da in self.deployed:
                    if da.component_id == cid and da.expected_available_date == current_date:
                        returning += da.quantity

                # Components returning from optimizer-assigned pours stripping on this date
                returning_from_pours = []
                for pi, pour in enumerate(pours):
                    if pour.available_date == current_date and cid in x[pi]:
                        returning_from_pours.append(x[pi][cid])

                # Components deployed to pours starting erection on this date
                deploying_to_pours = []
                for pi, pour in enumerate(pours):
                    if pour.erection_start_date == current_date and cid in x[pi]:
                        deploying_to_pours.append(x[pi][cid])

                if t == 0:
                    initial_qty = self.initial_inventory.get(cid, InventoryPosition(cid, 0, 0, 0)).qty_available
                    # I[c][0] = initial + bought + rented + returning - deployed
                    terms_plus = [b[cid][t], r[cid][t]]
                    terms_minus = deploying_to_pours

                    model.Add(
                        inv[cid][t]
                        == initial_qty + returning
                        + sum(1 * v for v in terms_plus)
                        + sum(1 * v for v in returning_from_pours)
                        - sum(1 * v for v in terms_minus)
                    )
                else:
                    model.Add(
                        inv[cid][t]
                        == inv[cid][t - 1] + returning
                        + b[cid][t] + r[cid][t]
                        + sum(1 * v for v in returning_from_pours)
                        - sum(1 * v for v in deploying_to_pours)
                    )

                # Non-negative inventory (already enforced by variable bounds, but explicit)
                model.Add(inv[cid][t] >= 0)

        # ============================================================
        # OBJECTIVE FUNCTION
        # ============================================================
        cost_terms = []
        S = self.COST_SCALE

        for cid in comp_ids:
            comp = self.components[cid]

            for t in range(self.num_periods):
                # Procurement cost
                buy_cost = int(comp.unit_cost_buy * S)
                if buy_cost > 0:
                    cost_terms.append(buy_cost * b[cid][t])

                # Rental cost (per day — approximate by period length)
                rent_cost = int(comp.unit_cost_rent_per_day * S)
                if rent_cost > 0:
                    cost_terms.append(rent_cost * r[cid][t])

                # Holding cost for idle inventory
                hold_cost = int(self.holding_cost_per_unit_per_day * S)
                cost_terms.append(hold_cost * inv[cid][t])

        # Handling cost per pour (based on assigned quantities)
        for pi, pour in enumerate(pours):
            for cid in x[pi]:
                comp = self.components.get(cid)
                if comp:
                    handle_cost = int(comp.weight_kg * self.handling_cost_per_kg * S)
                    if handle_cost > 0:
                        cost_terms.append(handle_cost * x[pi][cid])

        # Loss cost (expected loss per cycle)
        for pi, pour in enumerate(pours):
            for cid in x[pi]:
                comp = self.components.get(cid)
                if comp and comp.loss_rate_per_cycle > 0:
                    loss_cost = int(comp.loss_rate_per_cycle * comp.unit_cost_buy * S)
                    if loss_cost > 0:
                        cost_terms.append(loss_cost * x[pi][cid])

        model.Minimize(sum(cost_terms))

        # ============================================================
        # SOLVE
        # ============================================================
        solver = cp_model.CpSolver()
        solver.parameters.max_time_in_seconds = self.max_solve_time
        solver.parameters.num_workers = 4
        solver.parameters.log_search_progress = True

        logger.info("Starting CP-SAT solve...")
        status = solver.Solve(model)

        status_map = {
            cp_model.OPTIMAL: "OPTIMAL",
            cp_model.FEASIBLE: "FEASIBLE",
            cp_model.INFEASIBLE: "INFEASIBLE",
            cp_model.MODEL_INVALID: "INFEASIBLE",
            cp_model.UNKNOWN: "TIMEOUT",
        }
        result_status = status_map.get(status, "TIMEOUT")

        logger.info(f"Solve status: {result_status}, time: {solver.WallTime():.1f}s")

        if status not in (cp_model.OPTIMAL, cp_model.FEASIBLE):
            return OptimizationResult(
                run_id=run_id,
                project_id="",
                status=result_status,
                objective_value=0,
                solve_time_seconds=solver.WallTime(),
                optimality_gap=1.0,
                kit_assignments=[],
                procurement_plan=[],
                inventory_trajectory={},
                total_cost_breakdown={},
            )

        # ============================================================
        # EXTRACT SOLUTION
        # ============================================================
        kit_assignments = []
        procurement_plan = []
        total_procurement = 0.0
        total_rental = 0.0
        total_handling = 0.0
        total_holding = 0.0
        total_loss = 0.0

        for pi, pour in enumerate(pours):
            # Determine selected config
            selected_config = None
            if pour.is_locked and pour.locked_config_id:
                selected_config = next(
                    (cfg for cfg in pour.candidate_configs if cfg.config_id == pour.locked_config_id),
                    None
                )
            else:
                for config in pour.candidate_configs:
                    if config.config_id in z[pi] and solver.Value(z[pi][config.config_id]) == 1:
                        selected_config = config
                        break

            if not selected_config:
                continue

            # Build component assignments
            comp_assignments = []
            for cid, var in x[pi].items():
                qty = solver.Value(var)
                if qty <= 0:
                    continue

                comp = self.components.get(cid)
                if not comp:
                    continue

                # Determine source (simplified heuristic for solution extraction)
                # In production, track reuse flows explicitly via y variables
                source = "INVENTORY"
                reason = f"{comp.description} (qty: {qty}) — Assigned from available inventory."

                comp_assignments.append(ComponentAssignment(
                    component_id=cid,
                    quantity=qty,
                    source=source,
                    available_from=pour.erection_start_date,
                    cost=qty * comp.unit_cost_buy * comp.loss_rate_per_cycle + qty * comp.weight_kg * self.handling_cost_per_kg,
                    reason=reason,
                ))

            kit_assignments.append(KitAssignment(
                pour_id=pour.pour_id,
                selected_config_id=selected_config.config_id,
                component_assignments=comp_assignments,
                total_cost=sum(ca.cost for ca in comp_assignments),
                explanation=f"Config '{selected_config.description}' selected. Coverage: {selected_config.coverage_ratio:.2f}x.",
            ))

        # Extract procurement plan
        for cid in comp_ids:
            comp = self.components.get(cid)
            if not comp:
                continue
            for t in range(self.num_periods):
                buy_qty = solver.Value(b[cid][t])
                if buy_qty > 0:
                    cost = buy_qty * comp.unit_cost_buy
                    total_procurement += cost
                    procurement_plan.append(ProcurementPlan(
                        component_id=cid,
                        quantity=buy_qty,
                        action="BUY",
                        needed_by=self.date_list[t],
                        cost=cost,
                        justification=f"Procure {buy_qty}x {comp.description} by {self.date_list[t]} — insufficient inventory/reuse to cover demand.",
                    ))

                rent_qty = solver.Value(r[cid][t])
                if rent_qty > 0:
                    cost = rent_qty * comp.unit_cost_rent_per_day
                    total_rental += cost
                    procurement_plan.append(ProcurementPlan(
                        component_id=cid,
                        quantity=rent_qty,
                        action="RENT",
                        needed_by=self.date_list[t],
                        cost=cost,
                        justification=f"Rent {rent_qty}x {comp.description} for period starting {self.date_list[t]} — purchase not justified for remaining reuse.",
                    ))

        # Build inventory trajectory
        inventory_trajectory: dict[str, list[tuple[date, int]]] = {}
        for cid in comp_ids:
            trajectory = []
            for t in range(self.num_periods):
                qty = solver.Value(inv[cid][t])
                trajectory.append((self.date_list[t], qty))
            if any(qty > 0 for _, qty in trajectory):
                inventory_trajectory[cid] = trajectory

        objective_value = solver.ObjectiveValue() / self.COST_SCALE

        return OptimizationResult(
            run_id=run_id,
            project_id="",
            status=result_status,
            objective_value=objective_value,
            solve_time_seconds=solver.WallTime(),
            optimality_gap=solver.BestObjectiveBound() / max(solver.ObjectiveValue(), 1) if solver.ObjectiveValue() > 0 else 0,
            kit_assignments=kit_assignments,
            procurement_plan=procurement_plan,
            inventory_trajectory=inventory_trajectory,
            total_cost_breakdown={
                "procurement": total_procurement,
                "rental": total_rental,
                "handling": total_handling,
                "holding": total_holding,
                "expected_loss": total_loss,
                "total": objective_value,
            },
        )


