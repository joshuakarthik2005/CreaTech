# ROLLING HORIZON SOLVER (for large projects)
# ============================================================

class RollingHorizonSolver:
    """Solves large projects by windowed optimization.
    
    1. Fix decisions for already-executed pours
    2. Solve exact MIP for a 4-week lookahead window
    3. Use relaxed estimates for remaining pours
    4. Roll window forward weekly
    """

    def __init__(
        self,
        all_pours: list[Pour],
        components: list[Component],
        initial_inventory: list[InventoryPosition],
        deployed_allocations: list[DeployedAllocation],
        window_weeks: int = 4,
        step_weeks: int = 1,
    ):
        self.all_pours = sorted(all_pours, key=lambda p: p.planned_date)
        self.components = components
        self.initial_inventory = initial_inventory
        self.deployed_allocations = deployed_allocations
        self.window_weeks = window_weeks
        self.step_weeks = step_weeks

    def solve_rolling(self, current_date: date) -> list[OptimizationResult]:
        """Execute rolling-horizon optimization from current_date forward."""
        results = []
        window_start = current_date
        remaining_pours = [p for p in self.all_pours if p.planned_date >= current_date]

        if not remaining_pours:
            logger.info("No future pours to optimize.")
            return results

        project_end = max(p.planned_date for p in remaining_pours)

        while window_start <= project_end:
            window_end = window_start + timedelta(weeks=self.window_weeks)

            # Pours in this window — solve exactly
            window_pours = [p for p in remaining_pours
                            if window_start <= p.planned_date <= window_end]

            if not window_pours:
                window_start += timedelta(weeks=self.step_weeks)
                continue

            logger.info(
                f"Rolling horizon: optimizing {len(window_pours)} pours "
                f"from {window_start} to {window_end}"
            )

            optimizer = FormworkOptimizer(
                pours=window_pours,
                components=self.components,
                initial_inventory=self.initial_inventory,
                deployed_allocations=self.deployed_allocations,
                max_solve_time_seconds=60,  # Tighter budget per window
            )

            result = optimizer.solve()
            results.append(result)

            # Update inventory and deployed state for next window
            # (In production, this would update from the solution)

            window_start += timedelta(weeks=self.step_weeks)

        return results
